package com.jhbim.bimvr.dao.entity.vo;
//群好友管理vo类
public class GroupfriendVo {
    private String picture;
    private String name;
    private String roleimg;
    private String companyname;
    private String posotion;
    private String phone;

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getPicture() {
        return picture;
    }

    public void setPicture(String picture) {
        this.picture = picture;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getRoleimg() {
        return roleimg;
    }

    public void setRoleimg(String roleimg) {
        this.roleimg = roleimg;
    }

    public String getCompanyname() {
        return companyname;
    }

    public void setCompanyname(String companyname) {
        this.companyname = companyname;
    }

    public String getPosotion() {
        return posotion;
    }

    public void setPosotion(String posotion) {
        this.posotion = posotion;
    }
}
